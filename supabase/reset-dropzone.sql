do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'produtoras_perfis',
    'equipes_perfis',
    'jogadores_perfis',
    'managers_perfis',
    'campeonatos',
    'equipes',
    'campeonato_fases',
    'campeonato_equipes',
    'campeonato_grupos',
    'campeonato_slots',
    'campeonato_jogos',
    'campeonato_jogos_grupos',
    'convites_tokens',
    'jogadores_equipes',
    'campeonato_jogadores',
    'campeonato_regras',
    'campeonato_links'
  ]
  loop
    execute format('alter table public.%I enable row level security', table_name);
    execute format('drop trigger if exists trg_%I_updated_at on public.%I', table_name, table_name);
    execute format('create trigger trg_%I_updated_at before update on public.%I for each row execute function public.dropzone_set_updated_at()', table_name, table_name);
  end loop;
end $$;

